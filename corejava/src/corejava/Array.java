package corejava;

public class Array {

	public static void main(String[] args) {
	//declaring and  instantiating the array
		int a[]= {27,0,4,7,1};
		//initialization
		a[0]=12;
		a[1]=13;
		a[2]=15;
		//traversing the array
		for(int i=0;i<a.length;i++) {
			System.out.println(a[i]);
		}

	}

}
